              JR_COMMON.I$SDD_TRANSACTIONS C , JR_COMMON.I$SDD_PRODUCTS D 
        WHERE A.TRANSACTION_IRID = HEXTORAW('5D4748A4C7A7909EE0401490C2CF55B9')
          AND A.TRANSACTION_IRID = C.IRID 
          AND A.BRANCH_IRID      = B.IRID 
          AND B.PRODUCT_IRID     = D.IRID 
