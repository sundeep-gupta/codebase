
SELECT  A.BRANCH_IRID 
         FROM JR_COMMON.I$SDD_TRANS_BRANCH_MATRIX  A , JR_COMMON.I$SDD_BRANCHES B  ,
              JR_COMMON.I$SDD_TRANSACTIONS C , JR_COMMON.I$SDD_PRODUCTS D 
        WHERE C.NAME = 'mthaha_democheck_txn9'
          AND A.TRANSACTION_IRID = C.IRID 
          AND A.BRANCH_IRID      = B.IRID 
          AND B.PRODUCT_IRID     = D.IRID 
        ORDER BY D.NAME ;

