SELECT IRID, NAME, DATE_CREATED FROM   JR_COMMON.I$SDD_CONFIGURATIONS c
               WHERE  c.NAME like  'FUSIONAPPS_MAIN_LINUX%'
               AND    c.NAME not like  'FUSIONAPPS_MAIN_LINUX%.S'
               AND DATE_CREATED < '3-DEC-08';
               AND rownum < 2
               ORDER BY c.DATE_CREATED ASC ;
